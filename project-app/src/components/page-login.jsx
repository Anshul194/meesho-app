const defaultTheme = createTheme();

export default function PageLogin() {
  const handleSubmit = async (event) => {
    event.preventDefault();
    const data = new FormData(event.currentTarget);
    
    const formData = {
        email: data.get("email"),
        password: data.get("password"),
    };

    try {
        const response = await fetch(`${API_ENDPOINT}/api/v1/users/login`, {
            method: "POST",
            headers: {
                "Content-Type": "application/json"
            },
            body: JSON.stringify(formData)
        });

        if (!response.ok) {
            throw new Error("Failed to login");
        }

        const responseData = await response.json();
        if (responseData.success) {
            // Save the token to local storage
            localStorage.setItem('token', responseData.data.token);
            localStorage.setItem('user', responseData.data.userType); 
            localStorage.setItem('userId', responseData.data.userId);
            localStorage.setItem('clientId', responseData.data.clientId);
            localStorage.setItem('c_name', responseData.data.name); // Assuming the token is provided in the response
            // Redirect to the dashboard page upon successful login
            window.location.href = "/dashboard"; // Change "/dashboard" to your actual dashboard URL
        } else {
            // Handle login failure
            console.error("Login failed:", responseData.message);
            // You can display an error message to the user or take other actions as needed
        }
    } catch (error) {
        console.error("Error logging in:", error.message);
        // Handle the error (e.g., display an error message to the user)
    }
};



  return (
    <ThemeProvider theme={defaultTheme}>
      <Grid container component="main" sx={{ height: "100vh" }}>
        <CssBaseline />
        <Grid
          item
          xs={false}
          sm={4}
          md={7}
          sx={{
            backgroundImage:
              "url(https://source.unsplash.com/random?wallpapers)",
            backgroundRepeat: "no-repeat",
            backgroundColor: (t) =>
              t.palette.mode === "light"
                ? t.palette.grey[50]
                : t.palette.grey[900],
            backgroundSize: "cover",
            backgroundPosition: "center",
          }}
        />
        <Grid item xs={12} sm={8} md={5} component={Paper} elevation={6} square>
          <Box
            sx={{
              my: 8,
              mx: 4,
              display: "flex",
              flexDirection: "column",
              alignItems: "center",
            }}
          >
            <Avatar sx={{ m: 1, bgcolor: "secondary.main" }}>
              <LockOutlinedIcon />
            </Avatar>
            <Typography component="h1" variant="h5">
              Sign in
            </Typography>
            <Box
              component="form"
              noValidate
              onSubmit={handleSubmit}
              sx={{ mt: 1 }}
            >
              <TextField
                margin="normal"
                required
                fullWidth
                id="email"
                label="Email Address"
                name="email"
                autoComplete="email"
                autoFocus
              />
              <TextField
                margin="normal"
                required
                fullWidth
                name="password"
                label="Password"
                type="password"
                id="password"
                autoComplete="current-password"
              />

              <Button
                type="submit"
                fullWidth
                variant="contained"
                sx={{ mt: 3, mb: 2 }}
              >
                Sign In
              </Button>
            </Box>
          </Box>
        </Grid>
      </Grid>
    </ThemeProvider>
  );
}
